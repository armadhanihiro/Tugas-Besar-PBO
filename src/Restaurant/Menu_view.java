    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        try {
            JOptionPane.showMessageDialog(null, "Anda berhasil memesan, mohon ditunggu.");
            conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            stmt = conn.createStatement();
            String sql = "INSERT INTO pesanan (nama_pemesan, pesanan, total_harga) VALUES('"+nama+"','"+kata+"','"+jum+"')";
            stmt.executeUpdate(sql); 
            stmt.close();
            conn.close();
            Pembeli_view pembeli = new Pembeli_view();
            pembeli.setVisible(true);
            this.dispose();
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.print("Kesalahan Dalam Update Data");
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    
    
