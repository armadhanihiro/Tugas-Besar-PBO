    public void array_makanan(){
        try {
            conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            stmt = conn.createStatement();
            String sql = "SELECT * FROM makanan";
            rs = stmt.executeQuery(sql);
            while(rs.next()){
                listMakanan.add(new Makanan(rs.getString("nama_makanan"),rs.getInt("harga_makanan"),rs.getString("jenis_makanan")));
            }

            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
